package com.tnsif.DayEightArrayDemo;

public class Student {
	private int rolno;
	private String name;
	public Student(int rolno, String name) {
		super();
		this.rolno = rolno;
		this.name = name;
	}
	public int getRolno() {
		return rolno;
	}
	public void setRolno(int rolno) {
		this.rolno = rolno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
