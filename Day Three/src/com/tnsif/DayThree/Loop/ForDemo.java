package com.tnsif.DayThree.Loop;

public class ForDemo {

	public static void main(String[] args) {
		
		System.out.println("Even & Odd Numbers from 1 to 100 : ");
		for(int i=1;i<=100;i++)
		{
			if(i%2==0) 
			{
				System.out.println(i +" Even No");
			}
			else
			{
				System.out.println(i +" Odd No ");	
			}
		}

	}

}
